// load sections and buttons
// landing section
const landing = document.getElementById('landing')
// landing.style.display = "none"
const LandingPlayBtn = document.getElementById('landing_play')

// rules section
const rules = document.getElementById('rules')
rules.style.display = "none"
const rulesPlayBtn = document.getElementById('game_start')

// play section
const play = document.getElementById('play')
play.style.display = "none"
const nextBtn = document.getElementById('next')

// result section
const result = document.getElementById('result')
result.style.display = "none"
const replay = document.getElementById('replay')

// switch page display when button is clicked


// load picture screen and options from play section

const imgDisplay = document.getElementById('display')
const optionOne = document.getElementById('option_one')
const optionTwo = document.getElementById('option_two')
const optionThree = document.getElementById('option_three')

// both options
let options = document.querySelectorAll('.option')

// timer and image counter
const countDowntime = document.getElementById('timer')
const imageNum = document.getElementById('imageNum')

// load result page items
let medal = document.querySelector('.medal')
let finalCorrectTotal = document.getElementById('total')
let remark = document.getElementById('remark')


let index = 0
let timer = 0
let interval = 0
// this will be the score counter
let CorrectTotal = 0

// user answers stored here
let userAns = undefined

// create Timer
const countDown = () => {
    if (timer === 20) {
        // clearInterval(interval);
        endGame()
    } else {
        timer++
        countDowntime.innerText = timer;
    }
}

let getFellows = []

const loadData = () => {

    // let i = 0
    while (getFellows.length !== 10) {
        let rand = Math.floor(Math.random() * fellows.length)

        if (!getFellows.includes(fellows[rand])) {
            getFellows.push(fellows[rand])
        }
    }

    imageNum.textContent = index + 1
    imgDisplay.src = getFellows[index].url

    optionOne.textContent = getFellows[index].option1
    optionTwo.textContent = getFellows[index].option2
    optionThree.textContent = getFellows[index].option3


}

loadData()


// What happens when the play button on the landing section is clicked
LandingPlayBtn.addEventListener('click', () => {

    landing.style.display = "none"
    rules.style.display = "flex"
    play.style.display = "none"
    result.style.display = "none"
})

// What happens when the play button on the rules section is clicked
rulesPlayBtn.addEventListener('click', () => {

    landing.style.display = "none"
    rules.style.display = "none"
    result.style.display = "none"
    play.style.display = "flex"

    // setinterval
    interval = setInterval(countDown, 1000);
    loadData()

})


// check correct answer
options.forEach((option) => {
    option.addEventListener('click', () => {
        option.classList.add("active")
        if (option.textContent == getFellows[index].fullname) {
            CorrectTotal++
        } else {
            CorrectTotal += 0
        }

        // de-activate click
        for (i = 0; i <= 3; i++) {
            options[i].classList.add("disabled");
        }
    })
})

nextBtn.addEventListener("click", () => {
    // 
    if (index !== getFellows.length - 1) {
        index++
        options.forEach(removeActive => {
            removeActive.classList.remove("active");
        })
        loadData()

    } else {
        index = 0

        clearInterval(interval);
        play.style.display = "none"
        finalCorrectTotal.textContent = CorrectTotal

        endGame()

    }
    for (i = 0; i <= 3; i++) {
        options[i].classList.remove("disabled");
    }

})


const endGame = () => {
    clearInterval(interval);
    play.style.display = "none"
    finalCorrectTotal.textContent = CorrectTotal


    if (CorrectTotal >= 3 && CorrectTotal < 5) {
        medal.textContent = "🥉"
        remark.textContent = "👍🏽 You try, we will give you plastic 👍🏽"
    } else if (CorrectTotal >= 5 && CorrectTotal < 7) {
        medal.textContent = "🥈"
        remark.textContent = "👍🏽👍🏽 You try, you deserve some accolades 👍🏽👍🏽"
    } else if (CorrectTotal >= 7 && CorrectTotal < 10) {
        medal.textContent = "🥇"
        remark.textContent = "🙌🏽🙌🏽🙌🏽 Oshey Familiar Spirit 🙌🏽🙌🏽🙌🏽"
    } else if (CorrectTotal == 10) {
        medal.textContent = "🏆"
        remark.textContent = "🤝🏽🤝🏽🤝🏽 Give it up for Facebook 🤝🏽🤝🏽🤝🏽"
    } else {
        medal.textContent = "🎈"
        remark.textContent = "🚶🏽 Dey try come class 🚶🏽‍♂️"
    }

    result.style.display = "flex"


}

